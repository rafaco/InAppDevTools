package es.rafaco.inappdevtools.library.view.components.items;

import es.rafaco.inappdevtools.library.view.components.base.FlexData;

public class OverviewData extends FlexData {

    String title;
    String content;
    int icon;
    int color;
    Runnable performer;
    String performerText;

    public OverviewData(String title, String content, int icon, int color) {
        super();
        this.title = title;
        this.content = content;
        this.icon = icon;
        this.color = color;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public Runnable getPerformer() {
        return performer;
    }

    public void setPerformer(Runnable performer) {
        this.performer = performer;
    }

    public String getPerformerText() {
        return performerText;
    }

    public void setPerformerText(String performerText) {
        this.performerText = performerText;
    }
}
